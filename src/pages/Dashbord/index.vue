<script setup>
  import { computed, ref } from 'vue';
  import { useRouter } from 'vue-router';
  import { SideBar } from './ui';
  import dayjs from 'dayjs';
  import { pfm } from '@/shared/api';


  const user = JSON.parse(localStorage.getItem('user'));
  const todayDate = dayjs(new Date).format('DD.MM.YYYY');

  const router = useRouter();

  const courseId = ref('');
  const students = ref([]);

  const token = computed(() => {
    const user = JSON.parse(localStorage.getItem('user'));

    return user.token;
  });

  const isChanging = ref(false);
  const activeStudent = ref('');

  function getIconType(isAttend, type) {
    if (isAttend && type === 'CARD') {
      return 'CARD';
    }
    return isAttend ? 'bird' : 'red-bird';
  }

  function getStudents() {
    return pfm.admin
      .getStudents(courseId.value, token.value)
      .then(({ data }) => {
        students.value = data;
        return data;
      });
  }

  function changeAttendance(studentId, timeId, value) {
    isChanging.value = true;
    pfm.admin
      .changeAttendance(studentId, timeId, value, token.value)
      .then(() => {
        getStudents()
          .finally(() => isChanging.value = false);
      })      
  }
</script>